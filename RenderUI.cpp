RenderUI::RenderUI(HWND hwnd,D3DManager* d3dmanager,ImGuiIO& io,Renderer* renderer) : io(io)
{
    this->renderer = renderer;
    setStyle();
    io.Fonts->AddFontFromFileTTF("C://Users//admin123//source//repos//PafosGame//PafosGame//assets//Fonts//Roboto-Regular.ttf",16.0f);

    static const ImWchar icons_ranges[] = { ICON_MIN_FA, ICON_MAX_16_FA, 0 };
    ImFontConfig icons_config; icons_config.MergeMode = true; icons_config.PixelSnapH = true;

    io.Fonts->AddFontFromFileTTF("C://Users//admin123//source//repos//PafosGame//PafosGame//assets//Fonts//fa-regular-400.ttf", 14.0f, &icons_config, icons_ranges);
    
    // Setup Platform/Renderer backends
    ImGui_ImplWin32_Init(hwnd);
    ImGui_ImplDX11_Init(d3dmanager->getDevice(), d3dmanager->getDeviceContext());
}

RenderUI::~RenderUI()
{
    ImGui_ImplDX11_Shutdown();
    ImGui_ImplWin32_Shutdown();
    ImGui::DestroyContext();
}

void RenderUI::render()
{
    ImGui_ImplDX11_NewFrame();
    ImGui_ImplWin32_NewFrame();
    ImGui::NewFrame();

    ImGui::BeginMainMenuBar();
    ImGui::MenuItem("\xef\x85\x9b"" File");
    ImGui::MenuItem("\xef\x81\x84"" Edit");
    ImGui::MenuItem("?"" Help");
    ImGui::EndMainMenuBar();

}

void RenderUI::update(ID3D11RenderTargetView* renderTarget)
{
    renderer->render(renderTarget);
    
    ImGui::Render();
    ImGui_ImplDX11_RenderDrawData(ImGui::GetDrawData());
    if (io.ConfigFlags & ImGuiConfigFlags_ViewportsEnable)
    {
        ImGui::UpdatePlatformWindows();
        ImGui::RenderPlatformWindowsDefault();
    }
}

